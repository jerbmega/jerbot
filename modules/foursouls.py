    async def on_message(self, message):
        guilds = [123641386921754625, 485099143786922018, 273609483060641792]
        if not message.guild.id in guilds:
            return
        regex = re.compile('[^a-zA-Z0-9? ]')
        try:
            for string in re.findall('\[\[[^\]]*\]\]', message.content):
                string = regex.sub('', string.lower()[2:-2])
                for link in self.links:
                    for name in link[0]:
                        if string == name:
                            await message.channel.send(link[1])
                            raise Exception("Stop")
                for link in self.links:
                    for name in link[0]:
                        if name in string:
                            await message.channel.send(link[1])
                            raise Exception("Stop")
        except Exception:
            pass




